 			<h3>DIRECTIONS</h3>
				<h4>Red Wine Braised Short Ribs</h4>
			<p style="color:#000;margin-bottom:1.0em;font-size:0.9em;line-height:1.4em;">Before you begin, preheat the oven to 275F.</p>
     		<ol>
     			<li class="ingredients" />Season the short ribs with a little salt and pepper.
     			<li class="ingredients" />Peel and chop the carrot, onion, and celery into 1 inch pieces.
				<li class="ingredients" />Sear the ribs in a little oil in 2 large saute pans. [The idea is to develop a nice brown layer in the pans.] Remove them to a plate, but keep the pan hot.
				<li class="ingredients" />Toss the vegetables into the same hot pan and saute until they begin to color. Take care not to blacken the bottom of the pan.
				<li class="ingredients" />Add the tomato paste to each pan, letting it stick to the bottom and start to brown.
				<li class="ingredients" />Deglaze each pan with 1/2 the bottle of red wine. Scrape the pots with a wooden spoon to get all those brown goodies.
				<li class="ingredients" />Place the short rib pieces in an oven safe pot large enough to hold them in one layer.
				<li class="ingredients" />Add the wine-veg-tomato-paste-cooked-on-goodness mixture to the pot with the short ribs.
				<li class="ingredients" />Add the veal stock, garlic, bay leaves, and bring to a simmer.
				<li class="ingredients" />Cover the pan with a lid and place it in the oven. Cook it till fork tender. This could take up to 4 hours, depending on your oven, so plan accordingly.
				<li class="ingredients" />Allow them to cool enough to handle them. Remove them to a plate and keep warm.
				<li class="ingredients" />Do not throw away the cooking liquid. You will need it later.
			</ol>
    		<aside style="font-size:0.75em;padding-bottom:1.5em">Note on Stock: If you don't have veal or beef stock on hand, don't worry. You can
					just use water and still have a pretty good base for a sauce. If you have to go
					this route, a good trick is to throw a split pig trotter into the liquid. This will
					absolutely restore the body that you are sacrificing by not having proper
					stock. You can get a pig foot at your local grocery if you live in the south, like us,
					but otherwise you may have to head to the Asian or Mexican grocery. If you
					have never been to your local Asian or Mexican market, you are in for a treat!
			</aside>
    		<aside style="font-size:0.75em;padding-bottom:1.5em">Note on 'Fork Tender" This is the classic hallmark of whether a braised dish is
					done or not. What it means is that you can slide a carving fork (not a table fork)
					into the meat and then pull it out without the meat coming with it. If it clings, it
					needs more cook time. Never rush a braise. (If the bones fall out, you have
					gone too far, but that's ok. It will still be delicious.)
			</aside>
