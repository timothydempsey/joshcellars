 			<h3>DIRECTIONS</h3>
			<h4>Marinade</h4>
    			<ul >
     			<li class="directions" />Combine the marinade ingredients (garlic, thyme, rosemary, olive oil) in a small food processor.
				<li class="directions" />Puree until smooth.
    			<li class="directions" />Coat lamb loin with the marinade.
				<li class="directions" />Refrigerate for at least six hours or overnight.
     			
     		</ul>
     
	    	<h4>Spice Mix</h4>
			<ul >
     			<li class="directions" />Toast the spices.
     			<li class="directions" />Grind in mortar and pestle or in a small spice grinder.
				<li class="directions" />Reserve.
     		</ul>
			<h4>Lamb</h4>
			<ul>
				<li class="directions" />Prepare grill or heat pan over medium-high heat.
				<li class="directions" />Season marinated lamb with salt and some of the spice mix from above.
				<li class="directions" />Sear on all sides then move to the cooler part of the grill to finish cooking to medium rare. If cooking on a stovetop, place in a 400° oven for 4 to 5 minutes depending on size.
				<li class="directions" />Place lamb on a platter and cover with a piece of foil to allow meat to rest (5 – 10 minutes). 
				<li class="directions" />Slice fairly thin.
				<li class="directions" />Season with a little more salt and pepper and serve.
		  	</ul>
		  	<h4>Salsa Verde</h4>				
			<ul>
				<li class="directions"/>Rough chop all of the herbs
				<li class="directions"/>In a heavy duty blender, combine anchovies, garlic, capers, mustard, lemon juice and lemon zest. 
				<li class="directions"/>Puree until smooth.
				<li class="directions"/>Add the herbs and puree again until smooth.
				<li class="directions"/>Slowly add olive oil with blender on to emulsify.
				<li class="directions"/>Season with kosher salt and freshly cracked pepper.
		  	</ul>
			<h4>Charred broccoli with green olive vinaigrette</h4>
			<ul>
				<li class="directions"/>Prepare a grill or broiler on high heat.
				<li class="directions"/>Cut broccoli crowns lengthwise into long ‘planks’ about a 1/2” thick.
				<li class="directions"/>Drizzle with olive oil and season with salt and pepper. 
				<li class="directions"/>Cook for 1 – 2 minutes on each side.  It should be nicely charred on each side and tender, but still toothsome.
				<li class="directions"/>Quickly place into a bowl and dress liberally with vinaigrette, then add parsley. 
				<li class="directions"/>Arrange on a platter, sprinkle with radish, pecorino cheese then drizzle with a little more vinaigrette and serve. 
</ul>	
			<h4>Roasted green olive vinaigrette</h4>
			<ul>
				<li class="directions"/>Place olives on a heatproof dish and bake in 400&deg;F oven until shriveled (about 20 – 30 minutes).	
				<li class="directions"/>Place all ingredients except for vinegar, lemon juice, and olive oil in the bowl of the food processor.	
				<li class="directions"/>Grind to a paste. 
				<li class="directions"/>Add contents to a bowl with the lemon juice and champ vinegar. 
				<li class="directions"/>With a whisk, constantly stirring, stream in olive oil until incorporated. 
			</ul>	
			<h4>Farro Salad</h4>		
			<ul>
				<li class="directions"/>Bring about 4 cups of water to a boil in a medium saucepan.
				<li class="directions"/>Season water with salt and pepper and add bay leaves and faro. 
				<li class="directions"/>Simmer until farro is tender, but still has a little bite.  Strain farro and remove bay leaves.
						<ul>
						<li />This can be done ahead, chilled and then brought to room temperature.
						<li />Alternatively, reserve it room temperature until ready to serve.
						</ul>
				<li class="directions"/>Meanwhile, peel onions and cut into ¼” slices.  Drizzle with olive oil, season with salt and pepper and cook in 400 oven until charred and soft.  Remove, cool and mince.
				<li class="directions"/>In another heatproof dish, add Brussels leaves and drizzle with oil and season with salt and pepper.  When you begin making the salad, place in oven and bake until crispy.
				<li class="directions"/>Place farro into a bowl.  Add herbs and onions and enough dressing to coat.
				<li class="directions"/>Add turnips, radishes, carrots and beets and toss again, adding a little more dressing along with salt and some black pepper.
				<li class="directions"/>Fold in the Brussels sprout leaves, taste and adjust seasoning and add a little more dressing if needed.

			</ul>
				<h4>Sherry Vinaigrette</h4>
			<ul>
				<li class="directions"/>Place all ingredients, except olive oil, in a blender and puree until smooth.
				<li class="directions"/>Turn blender to low and add olive oil in a slow, steady stream through top of blender until emulsified. 
			</ul>
