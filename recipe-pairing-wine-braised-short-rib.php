			<div class="contentBlock">	
			<h3>DIRECTIONS</h3>
				<h4>Red Wine Braised Short Ribs</h4>
			<ul >
     			<li class="directions" />Before you begin, preheat the oven to 275&deg;F.
     			<li class="directions" />Season the short ribs with a little salt and pepper.
     			<li class="directions" />Peel and chop the carrot, onion, and celery into 1 inch pieces.
				<li class="directions" />Sear the ribs in a little oil in 2 large saute pans. Remove them to a plate, but keep the pan hot.
				<li class="directions" />Toss the vegetables into the same hot pan and saute until they begin to color. Take care not to blacken the bottom of the pan.
				<li class="directions" />Add the tomato paste to each pan, letting it stick to the bottom and start to brown.
				<li class="directions" />Deglaze each pan with 1/2 the bottle of red wine. Scrape the pots with a wooden spoon to get all those brown goodies.
				<li class="directions" />Place the short rib pieces in an oven safe pot large enough to hold them in one layer.
				<li class="directions" />Add the wine-veg-tomato-paste-cooked-on-goodness mixture to the pot with the short ribs.
				<li class="directions" />Add the veal stock, garlic, bay leaves, and bring to a simmer.
				<li class="directions" />Cover the pan with a lid and place it in the oven. Cook it till fork tender. This could take up to 4 hours, depending on your oven, so plan accordingly.
				<li class="directions" />Allow them to cool enough to handle them. Remove them to a plate and keep warm.
				<li class="directions" />Do not throw away the cooking liquid. You will need it later.
			</ul>
    		
	    	<h4>Cauliflower Puree</h4>
	    	<ul>
     			<li class="directions" />Break the cauliflower unto florets.
     			<li class="directions" />Add the pint of cream and water to cover.
				<li class="directions" />Simmer until the cauliflower is fully cooked and the liquid has boiled away to about a cup.
				<li class="directions" />Place it all in a blender and buzz. The cauliflower should turn into a smooth,
cream-colored puree.
				<li class="directions" />If the puree was cooked enough, you can get away without straining it. Just season with salt.
				<li class="directions" />While it is still warm, swirl in the butter.
				<li class="directions" />Keep warm
			</ul>
			<h4>Roasted Cauliflower</h4>
			<ul>
     			<li class="directions" />Before you begin, preheat your oven to 400&deg;F.
     			<li class="directions" />Heat a large saute pan and add a little oil.
     			<li class="directions" />Add the cauliflower and toss until it begins to brown in spots.
				<li class="directions" />Transfer to a sheet tray and bake until the cauliflower is cooked and nicely colored. You want to start checking it every 5 minutes after the first 10 minutes.
				<li class="directions" />When it is cooked, season it with a little salt and a drizzle of good olive oil.
			</ul>
			<h4>Charred Onion Jus</h4>
			<ul>
    			<li class="directions" />Before you begin, preheat your oven to 400&deg;F.
     			<li class="directions" />Sauté the onions in the oil until they begin to show color. (Its ok if they break up a little bit).
     			<li class="directions" />When the onions are getting nice and dark, add all the braising liquid and cook until it begins to thicken. You aren't looking for something you can stand a spoon up in, just a nice, dark, chunky sauce.
				<li class="directions" />When it has been fully cooked out, swirl in the butter. Season with salt and pepper if it needs it, but remember, the base liquid had the seasoned ribs in it, so you may not need it.
				<li class="directions" />A nice touch is to brighten the sauce up with a drip or two of red wine vinegar.
			</ul>
