		 	<div id="recipeCaption">Serves 6 | <a href="pdfs/recipes/josh-cellars-chipotle-grilled-chicken-thighs-recipe.pdf" style="color:#48191e;text-decoration:underline !important">Print-Quality (PDF)</a></div>
		 	
		 	<div class="ingredientsBlock">
					
					<h3>INGREDIENTS</h3>
					<ul style="padding-left:0;list-style-type:none">
					<li class="ingredients"/>6 bone-in chicken thighs (about 3 lbs.)
					<li class="ingredients"/>1 chipotle pepper in adobo sauce, finely chopped
					<li class="ingredients"/>1/3 cup honey
					<li class="ingredients"/>1/4 cup apple cider vinegar
					<li class="ingredients"/>1/4 cup chopped fresh cilantro 
					<li class="ingredients"/>4 garlic cloves, chopped fine (1 1/2 Tbsp.)
					<li class="ingredients"/>1 tsp. salt 
					<li class="ingredients"/>1/4 tsp black pepper 
			 		</ul>
					
			</div>		
			
			<div class="contentBlock">
			<h3>DIRECTIONS</h3>
    			<ul >
					<li class="directions" />To make marinade, combine chipotle pepper, honey, vinegar, cilantro and garlic in a large bowl. Remove 1/4 cup and set it aside to serve as a sauce.
					<li class="directions" />Place chicken in shallow dish or resealable plastic bag and pour marinade over chicken. Refrigerate 1 hour.  
					<li class="directions" />Heat grill to high, then lower setting to low. 
					<li class="directions" />Remove chicken from marinade and discard leftover marinade. Season both sides of chicken with salt and pepper. Place chicken skin side down on greased grill and cook 12-15 minutes, or until skin is golden. Turn chicken and continue to cook until internal temperature reaches 165°F.
					<li class="directions" />Serve chicken with reserved sauce.

    			</ul>
