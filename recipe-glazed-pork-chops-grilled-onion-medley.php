			<h3>DIRECTIONS</h3>
    			<ul >
     			<li class="directions" />To make marinade, combine vinegar, wine, rosemary, thyme, and garlic in a small saucepan. Place pan over medium heat, and cook for about 15 minutes until liquid is reduced to about 1/3 cup. Discard the rosemary and thyme, then place marinade in refrigerator to cool for 20 minutes. 
     					<li class="directions" />Place pork chops in shallow dish or resealable plastic bag and pour marinade over them. Marinate for 30 to 60 minutes.  
					    <li class="directions" />Combine salt and pepper in a small bowl.
						<li class="directions" />Heat grill to medium high. 
						<li class="directions" />Place onions and shallots in a medium bowl.  Drizzle with the olive oil and season with ½ teaspoon of the salt and pepper mixture. Place onions and shallots on grill and cook 10-12 minutes, turning occasionally, until tender. Remove from grill and set aside.
						<li class="directions" />Remove pork chops from marinade and discard any leftover marinade. Season pork chops on both sides with remaining salt and pepper mixture. Grill chops for 8-10 minutes, then turn and grill another 8-10 minutes or until an instant read thermometer reads 140° F. Remove chops, cover and let rest 4 minutes.
						<li class="directions" />If desired, sprinkle chops with extra chopped rosemary and thyme leaves. Serve with grilled onion medley. 

