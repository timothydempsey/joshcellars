    			<ul >
					<li class="directions" />Melt butter in large pot over medium heat. Add garlic and cook 2-3 minutes until light golden in color. Add wine, salt and pepper. 
					<li class="directions" />Increase heat to high, then add the mussels and stir well. Cover and cook 2 minutes. Stir again; then cover and cook 2-3 additional minutes or until mussel shells are opened. (Discard any mussels that do not open.) 
					<li class="directions" />Remove mussels to a bowl using a slotted spoon.  Add cream, parsley, and chives to the pot and bring to a boil over medium heat. Cook 2 minutes; then pour sauce over the mussels.
